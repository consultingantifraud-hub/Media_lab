        f"‚Ä¢ –í–∞—à Telegram ID: `{user_id}`\n"
        f"‚Ä¢ –í–∞—à Username: @{username}\n"
        "‚Ä¢ –í–∞—à –≤–æ–ø—Ä–æ—Å –∏–ª–∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ\n\n"
        "üí° **–ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤—ã:**\n"
        "‚Ä¢ –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫—É **ü§ñ –ò–ò-–ø–æ–º–æ—â–Ω–∏–∫** –¥–ª—è –≤–æ–ø—Ä–æ—Å–æ–≤ –æ —Ä–∞–±–æ—Ç–µ —Å–µ—Ä–≤–∏—Å–∞\n"
