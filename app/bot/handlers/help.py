async def handle_ai_assistant_message(message: types.Message, state: FSMContext) -> None:
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–æ–±—â–µ–Ω–∏–π –¥–ª—è –ò–ò-–ø–æ–º–æ—â–Ω–∏–∫–∞."""
    # –û–±—Ä–∞–±–æ—Ç–∫–∞ –≥–æ–ª–æ—Å–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
    if message.voice:
        await message.answer(
            "‚ùå **–ì–æ–ª–æ—Å–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—Ç—Å—è**\n\n"
            "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ—Ç–ø—Ä–∞–≤—å—Ç–µ –≤–∞—à –≤–æ–ø—Ä–æ—Å —Ç–µ–∫—Å—Ç–æ–º –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.",
            reply_markup=build_main_keyboard(),
            parse_mode="Markdown"
        )
        await state.clear()
        return
    
    if not message.text:
        return
    
    user_question = message.text.strip()
    if not user_question:
        return
    
    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –∫–Ω–æ–ø–∫–∏ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã - –æ–Ω–∏ –¥–æ–ª–∂–Ω—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å—Å—è –¥—Ä—É–≥–∏–º–∏ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞–º–∏
    if user_question in KEYBOARD_BUTTONS:
        logger.debug("AI Assistant: ignoring keyboard button: {}", user_question)
        # –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ, —á—Ç–æ–±—ã –¥—Ä—É–≥–∏–µ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –º–æ–≥–ª–∏ –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –∫–Ω–æ–ø–∫—É
        await state.clear()
        return
    
    # –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ –ë–î –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
    from app.db.base import SessionLocal
    from app.db.models import User, AiAssistantQuestion
    from app.services.billing import BillingService
    
    # –ü–æ–ª—É—á–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∑–∞—Ä–∞–Ω–µ–µ
    db_user = SessionLocal()
    user = None
    try:
        user, _ = BillingService.get_or_create_user(db_user, message.from_user.id, message.from_user)
    except Exception as e:
        logger.error("Error getting user for AI Assistant logging: {}", e, exc_info=True)
    finally:
        db_user.close()
    
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
    answer = ""
    error_msg = None
    
    logger.info("Processing AI Assistant question: length={}, user_telegram_id={}", len(user_question), message.from_user.id)
    
    try:
        await message.answer("ü§î –î—É–º–∞—é...")
        
        # –í—ã–∑—ã–≤–∞–µ–º LLM —á–µ—Ä–µ–∑ fal.ai
        payload = {
            "model": AI_ASSISTANT_MODEL,
            "prompt": user_question,
            "system_prompt": AI_ASSISTANT_SYSTEM_PROMPT,
            "temperature": 0.7,
            "max_tokens": 1000,
        }
        
        logger.info("AI Assistant request: {} (model: {})", user_question, AI_ASSISTANT_MODEL)
        response = await asyncio.to_thread(run_model, LLM_ENDPOINT, payload)
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –æ—Ç–≤–µ—Ç –∏–∑ –æ—Ç–≤–µ—Ç–∞ API
        if isinstance(response, dict):
            if "output" in response:
                output = response["output"]
                if isinstance(output, str) and output:
                    answer = output.strip()
                elif isinstance(output, dict) and "text" in output:
                    answer = output["text"].strip()
            elif "text" in response:
                answer = response["text"].strip()
            elif "content" in response:
                content = response["content"]
                if isinstance(content, str):
                    answer = content.strip()
                elif isinstance(content, list) and len(content) > 0:
                    first_item = content[0]
                    if isinstance(first_item, dict) and "text" in first_item:
                        answer = first_item["text"].strip()
        
        if not answer:
            answer = "–ò–∑–≤–∏–Ω–∏—Ç–µ, –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –æ—Ç–≤–µ—Ç. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∞—Ç—å –≤–æ–ø—Ä–æ—Å –∏–ª–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –≤ —Ç–µ—Ö–ø–æ–¥–¥–µ—Ä–∂–∫—É."
        
        await message.answer(answer, parse_mode="Markdown")
        
    except Exception as e:
        logger.error("Error in AI Assistant: {}", e, exc_info=True)
        error_msg = str(e)
        await message.answer(
            "‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞—â–µ–Ω–∏–∏ –∫ –ò–ò-–ø–æ–º–æ—â–Ω–∏–∫—É. "
            "–ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ –∏–ª–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –≤ —Ç–µ—Ö–ø–æ–¥–¥–µ—Ä–∂–∫—É.",
            reply_markup=build_main_keyboard()
        )
        await state.clear()
    
    # –õ–æ–≥–∏—Ä—É–µ–º –≤–æ–ø—Ä–æ—Å –≤ –ë–î (–¥–∞–∂–µ –µ—Å–ª–∏ –±—ã–ª–∞ –æ—à–∏–±–∫–∞)
    # –í–ê–ñ–ù–û: –õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –¥–æ–ª–∂–Ω–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç—å –í–°–ï–ì–î–ê, –Ω–µ–∑–∞–≤–∏—Å–∏–º–æ –æ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
    logger.info("Starting AI Assistant question logging...")
    db_log = SessionLocal()
    try:
        # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –±—ã–ª –ø–æ–ª—É—á–µ–Ω —Ä–∞–Ω–µ–µ, –ø–æ–ø—Ä–æ–±—É–µ–º –µ—â–µ —Ä–∞–∑
        if not user:
            logger.info("User not found, retrying to get/create user...")
            try:
                user, _ = BillingService.get_or_create_user(db_log, message.from_user.id, message.from_user)
                logger.info("User retrieved on retry: user_id={}", user.id if user else None)
            except Exception as e:
                logger.error("Error getting user for logging (retry): {}", e, exc_info=True)
        
        if user:
            logger.info("Creating AiAssistantQuestion record: user_id={}, question_length={}", user.id, len(user_question))
            question_log = AiAssistantQuestion(
                user_id=user.id,
                question=user_question,
                answer=answer if answer else None,
                error=error_msg
            )
            db_log.add(question_log)
            logger.info("Question log added to session, committing...")
            db_log.commit()
            logger.info("‚úÖ AI Assistant question logged successfully: id={}, user_id={}, question_length={}, has_answer={}, has_error={}", 
                       question_log.id, user.id, len(user_question), bool(answer), bool(error_msg))
        else:
            logger.warning("‚ö†Ô∏è Cannot log AI Assistant question: user not found for telegram_id={}", message.from_user.id)
    except Exception as e:
        logger.error("‚ùå Error logging AI Assistant question: {}", e, exc_info=True)
        import traceback
        logger.error("Traceback: {}", traceback.format_exc())
        db_log.rollback()
    finally:
        db_log.close()
        logger.info("Database session closed for AI Assistant logging")


